const isSatisfy = (springs, info) => {
	const springsUpdated = springs.replace(/\0+/g, '0');
	const groups = springsUpdated.split('0');
	const infoFromSprings = groups.map((group) => group.length).filter(Boolean);

	if (infoFromSprings.length !== info.length) return false;

	return infoFromSprings.every((ifs, i) => ifs === info[i]);
};

const getAllStrings = (all, ones, inputPrefix) => {
	const prefix = inputPrefix || '';

	if (ones === 0) return [prefix + ''.padEnd(all, '0')];
	if (ones === all) return [prefix + ''.padEnd(all, '1')];

	if (ones === 1) {
		const res = [];
		for (let i = 0; i < all; i++) {
			res.push(prefix + ''.padEnd(i, '0') + '1' + ''.padEnd(all - i - 1, '0'));
		}
		return res;
	}

	if (all - ones === 1) {
		const res = [];
		for (let i = 0; i < all; i++) {
			res.push(prefix + ''.padEnd(i, '1') + '0' + ''.padEnd(all - i - 1, '1'));
		}
		return res;
	}

	return [
		...(all > ones ? getAllStrings(all - 1, ones, `${prefix}0`) : []),
		...getAllStrings(all - 1, ones - 1, `${prefix}1`),
	];
};

const makeString = (info, dots) => {
	let str = ''.padEnd(dots[0], '0');
	for (let i = 0; i < info.length; i++) {
		str = str + ''.padEnd(info[i], '1') + ''.padEnd(dots[i + 1], '0');
	}
	return str;
};

