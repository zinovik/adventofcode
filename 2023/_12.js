const getAllInfoVariants = (info, length) => {
	const onesCount = info.reduce((s, i) => s + i, 0);
	const dotGroups = info.length - 1;
	const dotsCount = length - onesCount;

	const dots = [];

	dots.push(0);
	for (let i = 0; i < dotGroups; i++) {
		dots.push(1);
	}
	dots.push(0);

	const maxOneGroup = dotsCount - dotGroups + 1;

	const variants = [];

	if (dots.reduce((s, iii) => s + iii, 0) === dotsCount) {
		variants.push(makeString(info, dots));
	}

	let ind = dots.length - 1;
	while (dots[0] !== maxOneGroup) {
		if (dots[ind] >= maxOneGroup) {
			ind = ind - 1;
		} else {
			dots[ind] = dots[ind] + 1;
			for (let i = ind + 1; i < dots.length; i++) {
				dots[i] = i === dots.length - 1 ? 0 : 1;
			}
			ind = dots.length - 1;
			let sum = 0;
			for (let ss = 0; ss < dots.length; ss++) {
				sum = sum + dots[ss];
			}
			if (sum === dotsCount) {
				console.log(dots);
				variants.push(makeString(info, dots));
			}
		}
	}

	return variants;
};

const getAllValidOptions = (springs, info) => {
	let opts = 0;

	const total1required = info.reduce((s, e) => s + e, 0);

	if (total1required + total1required.length - 1 === springs.length) return 1;

	const questions = springs.length - springs.replaceAll('?', '').length;

	const total1origin = springs.length - springs.replaceAll('1', '').length;

	const allStrings = getAllStrings(questions, total1required - total1origin);

	allStrings.forEach((bin) => {
		let qi = 0;
		let str = '';

		for (let j = 0; j < springs.length; j++) {
			if (springs[j] === '?') {
				str = str + (bin[qi] || '0');
				qi++;
			} else {
				str = str + springs[j];
			}
		}
		if (isSatisfy(str, info)) {
			opts = opts + 1;
		}
	});

	return opts;
};

const getAllValidOptions2 = (springs, info) => {
	const variants = getAllInfoVariants(info, springs.length);

	let opts = 0;

	variants.forEach((variant) => {
		if (isCanBe(springs, variant)) {
			opts = opts + 1;
		}
	});

	return opts;
};

